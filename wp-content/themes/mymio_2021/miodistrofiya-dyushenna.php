                        <h2>Лечение мышечной дистрофии Дюшенна</h2>
                    </div>
                    <strong style="display: block; margin-top: 53px; margin-bottom: 15px;">Глюкокортикоиды (или глюкортикостеороиды)</strong>
                    <p>Применяемое в настоящее время противовоспалительное лечение кортикостероидами является золотым стандартом, описанным в международном руководстве по ведению пациентов с миодистрофией Дюшенна.</p>
                    <p>Данные рандомизированных контролируемых исследований показали пользу кортикостероидов для восстановления мышечной силы. Кроме основного противовоспалительного эффекта (снимает отек и воспаление, стабилизирует мышечную мембрану), терапия кортикостероидами позволяет пациенту продлить время способности к самостоятельному передвижению (ходьбы). В настоящее время прием кортикостероидов сохраняется даже после потери пациента способности ходить. Продолжение терапии поможет сдерживать развитие сколиоза, сохранять мышечную силу в верхних конечностях, поддержать дыхательную и сердечную функцию. Поскольку кортикостероиды используются пациентами с миодистрофией Дюшенна в течение длительного времени, важен мониторинг нежелательных явлений и осложнений такой терапии (например, набор веса, изменения в поведении, задержку роста и\или полового созревания и др.).</p>
                    <strong>Кардиопротективная терапия</strong>
                    <p>Кардиомиопатия и сердечная недостаточность являются основными причинами смертности у пациентов с МДД.</p>
                    <p>Целью кардиопротективной терапии является снижение нагрузки на миокард. Доказана эффективность назначения ингибиторов АПФ для профилактики дилатационной кардиомиопатии. Например, одним из первых было исследование Института миологии в Париже показавшее, что при раннем назначении ингибиторов АПФ к 15 годам кардиомиопатия сформировалась всего у 20–30 процентов пациентов вместо 70 процентов, как было раньше. Также назначаются препараты, снижающие частоту сердечных сокращений – бета-блокаторы. Это относится к стадии компенсации.</p>
                    <strong>Кардиометаболическая терапия (L-карнитин, коэнзим Q10)</strong>
                    <p>На стадии декомпенсации специалист может рекомендовать дополнительные препараты (например, кардиотоники).</p>
                    <strong>Метаболическая терапия</strong>
                    <p>Для профилактики остеопороза показано назначение препаратов, содержащих витамин D3 и кальций.</p>
                    <strong>Генная терапия</strong>
                    <p>Цель этиотропного лечения устранить причину заболевания — мутацию в гене DMD, тем самым восстановить синтез дистрофина. В настоящее время в мире одобрено несколько препаратов, восстанавливающих синтез белка.</p>
                    <strong>Нонсенс мутация. Стоп-кодон</strong>
                    <p>Приблизительно 10–15% МДД вызваны точечными мутациями, приводящими к образованию преждевременного стоп-кодона, то есть, преждевременной остановке синтеза белка дистрофина. Аталурен (Translarna, PTC Therapeutics, США) разработан для пропуска преждевременных стоп-кодонов. <b>Аталурен</b> связывается с рибосомной РНК и ухудшает распознавание преждевременного стоп-кодона, что позволяет восстановить трансляцию и синтез модифицированного дистрофина.</p>
                    <strong>Пропуск экзона/ов</strong>
                    <p>Нарушение рамки считывания (процесса синтеза белка дистрофин) в большинстве случаев связано с делециями и дупликациями, поэтому основной разрабатываемой стратегией генотерапии являлся метод пропуска экзона или экзонов. Такой метод приводит к восстановлению рамки считывания, иными словами – возобновлению экспрессии укороченного дистрофина.  Восстановление экспрессии частично функционального дистрофина должно преобразовать злокачественную форму МДД с тяжелым фенотипическим проявлением в МДБ – мышечную дистрофию Беккера. Данный метод лечения может помочь большинству пациентов с мышечной дистрофией Дюшенна. Исключение составят делеции, разрушающие актин-связывающие домены в N-концевой области или затрагивающие первый или последний экзоны и крупные хромосомные перестройки. Такие мутации достаточно редки и вместе составляют менее 10% от всех описанных мутаций в гене DMD. В настоящее время одобрены три препарата, использующие метод пропуска экзона с помощью антисмысловых олигонуклеотидов АОН.</p>
                    <strong>Ведутся и другие разработки препаратов генной терапии</strong>
                    <p>Самым ожидаемым является универсальный препарат или метод, который сможет применяться при любом виде мутации в гене дистрофин.</p>
                    <p>Таким препаратом можно назвать мини и <b>микродистрофин</b> или метод <b>CRISPR/Cas9</b>.</p>
