                        <div class="heading-article mio">
                        <h2>Диагностика мышечной дистрофии Дюшенна</h2>
                    </div>
                    <p>Первая ступень диагностики при подозрении на миодистрофию Дюшенна·   <b>Исследование активности КФК сыворотки крови.</b></p>
                    <p>При миопатии Дюшенна происходит распад миоцитов (рабдомиолиз) и высвобождению в кровь КФК и других продуктов цитолиза. Поэтому уровень КФК крови значительно повышен. Отклонения уровня активности КФК в 100 и более раз. Таким образом, исследование активности КФК сыворотки крови может быть первый шагом в диагностике заболевания.</p>
                    <strong>Показатели ЛДГ, АЛТ, АСТ</strong>
                    <p>Кроме определения КФК в биохимическом анализе крови необходимо определять активность таких ферментов как лактатдегидрогеназа (ЛДГ), и, так называемых, «печеночных ферментов» аланинаминотрансферазы (АЛТ) и аспартатаминотрансферазы (АСТ). При ПМД Дюшенна активность фермента ЛДГ бывает повышена в 3-5 раз. Активность ферментов АСТ и АЛТ, имеющих внепеченочное происхождение, может быть повышена в десятки раз.</p>
                    <p><u><b>Для верификации диагноза</b> Прогрессирующая мышечная дистрофия Дюшенна необходима <b>генетическая диагностика.</b></u></p>
                    <strong>Проведение ДНК анализа</strong>
                    <p>Выполнение генетического исследования начинают обычно с простого метода MLPA (Multiplex ligation-dependent probe amplification – англ., множественная лиганд-зависимая проба амплификации), позволяющего проверить наличие всех 79 экзонов в гене <i>DMD – так может быть обнаружена <b>делеция</b> экзона/ов.</i></p>
                    <p>Выявление <b>дупликаций</b> проводится посредством модифицированной MLPA. Если мутация так и не была выявлена применяют <b>секвенирование</b> гена DMD, для выявления точечных мутаций. Различные виды генетических исследований позволяют получить более подробную информацию об изменениях структуры (мутациях) ДНК при ПМД Дюшенна. Для проведения исследования необходимо 2 мл венозной крови. Подтверждение диагноза результатами генетического исследования позволяет включить ребенка в клинические исследования, а для его родителей выработать рекомендации по пренатальной диагностике при следующих беременностях. После определения мутации (изменения гена дистрофина), матери ребенка предлагается пройти генетическое исследование для выявления носительства мутации в гене DMD. Такая информация важна для других родственников женского пола со стороны матери (ее сестер, дочерей, теток, двоюродных сестер), поскольку они также могут являться носителями данной мутации.</p>
                    <p>Возможно проведение пренатальной диагностика ПМД Дюшенна методами молекулярно-генетического исследования (материал для исследования у матери необходимо забирать до 12 недели беременности), а также предимплантационная генодиагностика в случае проведения экстракорпорального оплодотворения.</p>
